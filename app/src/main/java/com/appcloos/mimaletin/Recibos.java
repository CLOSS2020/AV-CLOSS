package com.appcloos.mimaletin;

public class Recibos {
    String codigoCliente;
    String nombreCliente;
    String nroRecibo;
    String codigoVendedor;
    String fechaRecibo;
    String statusRecibo;
    String montoRecibo;

    public String getCodigoCliente() {
        return codigoCliente;
    }

    public void setCodigoCliente(String codigoCliente) {
        this.codigoCliente = codigoCliente;
    }

    public String getNombreCliente() {
        return nombreCliente;
    }

    public void setNombreCliente(String nombreCliente) {
        this.nombreCliente = nombreCliente;
    }

    public String getNroRecibo() {
        return nroRecibo;
    }

    public void setNroRecibo(String nroRecibo) {
        this.nroRecibo = nroRecibo;
    }

    public String getCodigoVendedor() {
        return codigoVendedor;
    }

    public void setCodigoVendedor(String codigoVendedor) {
        this.codigoVendedor = codigoVendedor;
    }

    public String getFechaRecibo() {
        return fechaRecibo;
    }

    public void setFechaRecibo(String fechaRecibo) {
        this.fechaRecibo = fechaRecibo;
    }

    public String getStatusRecibo() {
        return statusRecibo;
    }

    public void setStatusRecibo(String statusRecibo) {
        this.statusRecibo = statusRecibo;
    }

    public String getMontoRecibo() {
        return montoRecibo;
    }

    public void setMontoRecibo(String montoRecibo) {
        this.montoRecibo = montoRecibo;
    }
}

